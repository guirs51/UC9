    private void buttonEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEntrarActionPerformed
        String email = gmail.getText();
        
        char[] passwordArrya = password.getPassword(); 
        
        String senha = new String(passwordArrya);
        
        String name = ListarUsuario.buscarUsuario(conexao, email, senha);
        
        if ( name != null) {
            new TelaTeste(name).setVisible(true); // abrir a nova tela
            this.dispose(); // fechar a tela atual
        }
    }//GEN-LAST:event_buttonEntrarActionPerformed

